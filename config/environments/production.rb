    api_key: ENV.fetch("RESEND_API_KEY"),
    from: "Toaster <toaster@ericdahl.dev>"
  }
  config.action_mailer.raise_delivery_errors = true
  config.action_mailer.default_url_options = { host: ENV.fetch("APP_HOST", "toaster.app") }

